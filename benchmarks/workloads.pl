% benchmarks/workloads.pl - the PRODUCT / performance-tracking manifest for arrange.
%
% A "cost ladder" of mesh puzzles whose arrange search cost climbs from ~0.1M to
% ~182M inferences across THREE realistic grid sizes (9x9 mini, 15x15 daily, 21x21
% Sunday). This is the product bench's single source of truth (run_arrange.pl reads
% only this file), deliberately separate from benchmarks/fixtures.pl (the strategy-
% research matrix) so a research knob can never leak into the product numbers (plan
% §6). No strategy column exists here - arrange always runs its production mrv_inc
% 4-corner search.
%
% WHY A LADDER (the hill-climbing charter): arrange search cost (inferences) is
% deterministic and machine-INDEPENDENT - the SAME count native or under WASM, only
% the wall-per-inference constant changes. So a `-X% inferences` win here predicts a
% ~X% WASM speedup. benchmarks/baseline.json records each rung and `make bench-check`
% reports the delta (ratchet: regressions fail, improvements are wins you accept
% with `make bench-record`).
%
% WHY THREE SIZES: grid size is a COVERAGE axis, not a difficulty axis (difficulty
% comes from word density + small alphabet, per the gen_mesh_fixture.py K knob). But
% the algorithm BEHAVES differently by size - branching fan-out, the 4-corner start
% logic, and N^2 memory all scale with the grid - so a change that wins at 15x15 can
% regress at 21x21. 15x15 (the target size) keeps a deep density ladder; 9x9 and
% 21x21 each get a light + hard pair as size sentinels. Empirically each size has a
% difficulty CLIFF (a small grid packed with words either resolves fast or thrashes):
% 9x9 completes to ~3M then cliffs (~16 words), 21x21 to ~28M then cliffs (~80 words),
% so each size's "hard" rung is the densest that still completes deterministically.
%
% All rungs are generated by benchmarks/gen_mesh_fixture.py G N K 3 4 11 (seed 11):
% a planted-witness mesh, satisfiable + reachable by construction, so every rung
% places all N words. Rungs were chosen by their MEASURED arrange cost.
%
% arrange_workload(Fixture, Size, Mode, Iterations, Warmup, Expected, Tier, Budget)
%   Fixture    - path relative to the repo root; read by BOTH layers
%   Size       - the grid N (== the fixture's generated grid); arrange --size N
%   Mode       - size (fixed NxN canvas)
%   Iterations - measured repetitions
%   Warmup     - unmeasured warmup repetitions. Load-bearing for exactness: cold vs
%                warm differ by a fixed ~7.6k (first-call JIT indexing), a 1.7% wobble
%                on a ~0.45M rung. Fast rungs warm (warmup >= 1, exact); the two
%                slowest rungs use warmup 0 iters 1 (their ~7.6k cold/warm delta is
%                < 0.01% of the count - absorbed by the ratchet tolerance - and
%                warming them would double an 8s run).
%   Expected   - placed | infeasible; asserted every iteration, both layers
%   Tier       - core | heavy
%       core  : sub-~0.3 s light/moderate rungs; run by default (make bench / bench-check).
%       heavy : the hard tail (per-size dense rungs + the 15x15 extreme); opt in with
%               --heavy (or --fixture). Still deterministic + ratcheted, just slow.
%   Budget     - per-operation inference budget for the SEARCH layer
%                (arrange_best_layout/6), raised above the shipped 500M default so a
%                hard rung runs to true completion (a deterministic, ratchetable count)
%                instead of saturating. 2e9 is ample for this ladder (top rung ~182M).
%
% Order matters: all core rungs precede the heavy rungs, so a --heavy run has already
% warmed the process before the warmup-0 rungs are measured.

% -- core: fast, run by default (one light rung per size + the 15x15 mid-ladder) --
arrange_workload('fixtures/ladder_09x09_08w.pl', 9,  size, 20, 3, placed, core,  2_000_000_000).  % 9x9  light,  ~0.10M inf
arrange_workload('fixtures/ladder_15x15_12w.pl', 15, size, 20, 3, placed, core,  2_000_000_000).  % 15x15 light,  ~0.45M inf
arrange_workload('fixtures/ladder_21x21_25w.pl', 21, size, 20, 3, placed, core,  2_000_000_000).  % 21x21 light,  ~1.26M inf
arrange_workload('fixtures/ladder_15x15_28w.pl', 15, size, 15, 3, placed, core,  2_000_000_000).  % 15x15 mid,    ~1.66M inf
arrange_workload('fixtures/ladder_15x15_32w.pl', 15, size, 10, 3, placed, core,  2_000_000_000).  % 15x15 mid,    ~4.80M inf

% -- heavy: the hard tail per size; opt in with --heavy -------------------------
arrange_workload('fixtures/ladder_09x09_16w.pl', 9,  size,  3, 2, placed, heavy, 2_000_000_000).  % 9x9  hard,   ~3.06M inf (near its cliff)
arrange_workload('fixtures/ladder_21x21_80w.pl', 21, size,  2, 1, placed, heavy, 2_000_000_000).  % 21x21 hard, ~27.8M inf, ~1.4 s
arrange_workload('fixtures/ladder_15x15_34w.pl', 15, size,  1, 0, placed, heavy, 2_000_000_000).  % 15x15 hard, ~68.5M inf, ~3.2 s
arrange_workload('fixtures/ladder_15x15_36w.pl', 15, size,  1, 0, placed, heavy, 2_000_000_000).  % 15x15 xhard, ~182M inf, ~8.7 s

% -- heavy: post-campaign envelope guards (P2 probe, 2026-07-04). One rung per
% size just past the PRE-campaign cliff, all completing with large headroom
% under the shipped 5e8 budget on the post-E-H1..E-H7 engine. Generated
% gen_mesh_fixture.py G N K 3 4 11: (9 17 5), (15 40 6), (21 82 6). The
% near-cliff regime beyond these is instance-noisy + search-bound (see
% benchmarks/results/2026-07-04-p2-envelope-probe.md) - do NOT promote
% marginal ~5e8 instances to rungs; they would flip on any solver change.
arrange_workload('fixtures/ladder_09x09_17w.pl', 9,  size,  1, 0, placed, heavy, 2_000_000_000).  % 9x9 xhard, past the old cliff, ~46.7M inf, ~2.5 s
arrange_workload('fixtures/ladder_15x15_40w.pl', 15, size,  2, 1, placed, heavy, 2_000_000_000).  % 15x15 density sentinel, ~13.5M inf, ~0.7 s
arrange_workload('fixtures/ladder_21x21_82w.pl', 21, size,  2, 1, placed, heavy, 2_000_000_000).  % 21x21 past the old "hard-for-all" line, ~10.8M inf, ~0.6 s
