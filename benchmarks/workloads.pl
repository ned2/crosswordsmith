% benchmarks/workloads.pl - the PRODUCT / performance-tracking manifest for arrange.
%
% A "cost ladder" of mesh puzzles whose gated arrange search cost climbs from
% ~0.025M to ~38.5M inferences across THREE realistic grid sizes (9x9 mini, 15x15 daily, 21x21
% Sunday). This is the product bench's single source of truth (run_arrange.pl reads
% only this file), deliberately separate from benchmarks/fixtures.pl (the strategy-
% research matrix) so a research knob can never leak into the product numbers (plan
% §6). No strategy column exists here - arrange always runs its production mrv_inc
% strict search over two non-transpose corner representatives.
%
% WHY A LADDER (the hill-climbing charter): arrange search inference counts are
% stable regression signals under the SWI version recorded in baseline.json.
% `make bench-check` reports each rung's delta; same-version regressions fail and
% improvements are wins you accept with `make bench-record`. Native/WASM parity
% is measured separately by wasm/test/inference_parity.pl.
%
% WHY THREE SIZES: grid size is a COVERAGE axis, not a difficulty axis (difficulty
% comes from word density + small alphabet, per the gen_mesh_fixture.py K knob). But
% the algorithm BEHAVES differently by size - branching fan-out, the strict-corner
% logic, and N^2 memory all scale with the grid - so a change that wins at 15x15 can
% regress at 21x21. 15x15 (the target size) keeps a deep density ladder; 9x9 and
% 21x21 each get a light + hard pair as size sentinels. Empirically each size has a
% difficulty CLIFF (a small grid packed with words either resolves fast or thrashes):
% 9x9 completes to ~3M then cliffs (~16 words), 21x21 to ~28M then cliffs (~80 words),
% so each size's "hard" rung is the densest that still completes deterministically.
%
% The ladder_* rungs are generated by benchmarks/gen_mesh_fixture.py G N K 3 4 11
% (seed 11): a planted-witness mesh, satisfiable + reachable by construction, so
% every rung places all N words. Rungs were chosen by their MEASURED arrange cost.
%
% REALISM ANCHORS (rework plan par.6/par.9, the stress-test M1/M2 correction): the
% real_* rungs are real ENABLE dictionary words (a long anchor + 4-9 letter fills)
% planted on the same witness construction (benchmarks/gen_real_fixture.py), at the
% blocked-standard daily sizes 13/15 - inputs shaped like what a user actually feeds
% arrange, complementing the synthetic ladder (which owns the cost axis). Real-word
% instances near the density cliff are wildly instance-noisy (probed 2026-07-06:
% 15x15 12w=1.7M / 14w=85K / 16w=853M / 18w=248K warm inf), so the anchors are
% deliberately well-behaved instances away from the thrash regime - do NOT promote
% a thrash-regime real instance to a rung; its count would swing on any solver
% change. The old benchmark_20/26 fixtures stay OUT of product scope (size-brittle
% single-corner skeletons that place only at 37/49 - plan par.6); they remain
% research-matrix fixtures in benchmarks/fixtures.pl.
%
% LATENCY PROBE: benchmark_16_dense saturates the SHIPPED 500M budget (its count
% pins to the budget constant - verified 2026-07-06: outcome placed, 500,000,190
% inf, ~30-40 s) - degenerate as an inference signal, but it IS the user-felt
% worst-case cliff the README documents (two representatives share one budget; a deep
% corner burns it even after a layout was found). It runs as gate=latency: wall
% reported, search_inf info-only, never ratchet-gated.
%
% arrange_workload(Fixture, Size, Mode, Iterations, Warmup, Expected, Tier, Gate,
%                  Budget, Words)
%   Fixture    - path relative to the repo root; read by BOTH layers
%   Size       - the grid N (== the fixture's generated grid); arrange --size N
%   Mode       - size (fixed NxN canvas)
%   Iterations - measured repetitions
%   Warmup     - unmeasured warmup repetitions. Load-bearing for exactness: cold vs
%                warm differ by a fixed ~7.6k (first-call JIT indexing), a 1.7% wobble
%                on a ~0.45M rung. Fast rungs warm (warmup >= 1, exact); the two
%                slowest rungs use warmup 0 iters 1 (their ~7.6k cold/warm delta is
%                < 0.01% of the count - absorbed by the ratchet tolerance - and
%                warming them would double an 8s run).
%   Expected   - placed | infeasible; asserted every iteration, both layers
%   Tier       - core | heavy
%       core  : sub-~0.3 s light/moderate rungs; run by default (make bench / bench-check).
%       heavy : the hard tail (per-size dense rungs + the 15x15 extreme); opt in with
%               --heavy (or --fixture). Still deterministic + ratcheted, just slow.
%   Gate       - inf | latency (rework plan par.6: a budget-saturated cell's count is
%                pinned to the budget constant - degenerate as a signal)
%       inf     : search_inf is ratchet-gated by check_baseline (the normal case).
%       latency : search_inf is reported info-only and can neither fail nor win the
%                 ratchet; the rung exists for its wall-latency (the user-felt cliff).
%   Budget     - per-operation inference budget for the SEARCH layer
%                (arrange_best_layout/6), raised above the shipped 500M default so a
%                hard rung runs to true completion (a deterministic, ratchetable count)
%                instead of saturating. 2e9 is ample for this ladder (top gated rung ~38.5M).
%                The latency-gated saturation probe instead uses exactly the shipped
%                500M default, because the user-felt latency of saturation IS its point.
%   Words      - exact fixture word count, asserted by both benchmark runners so a
%                truncated or replaced fixture cannot silently change the subject.
%
% Order matters: all core rungs precede the heavy rungs, so a --heavy run has already
% warmed the process before the warmup-0 rungs are measured.

